// ------------------------------------------
// WHAT ARE LOOPS?
// ------------------------------------------
// Loops allow you to execute a block of code repeatedly
// without having to write the same code over and over again

// ------------------------------------------
// TYPES OF LOOPS IN JAVASCRIPT
// ------------------------------------------

// 1. FOR LOOP
// Used when you know how many times you want to repeat
// Syntax: for (init; condition; increment) { }

// 2. WHILE LOOP
// Repeats as long as a condition is true
// Syntax: while (condition) { }

// 3. DO...WHILE LOOP
// Executes at least once, then repeats if condition is true
// Syntax: do { } while (condition);

// 4. FOR...IN LOOP
// Loops through object properties and array indices
// Syntax: for (key in object) { }

// 5. FOR...OF LOOP
// Loops through values in an iterable (array, string, etc)
// Syntax: for (value of iterable) { }

// 6. FOREACH METHOD
// Executes a function for each array element
// Syntax: array.forEach(function(element) { });

// ------------------------------------------
// LOOP CONTROL STATEMENTS
// ------------------------------------------

// BREAK
// Terminates the loop immediately
// Used when you want to exit the loop early based on a condition

// CONTINUE
// Skips the current iteration and jumps to the next one
// Used to skip specific iterations without exiting the loop

// ------------------------------------------
// NESTED LOOPS
// ------------------------------------------
// A loop within another loop
// Used for multi-dimensional arrays or complex iterations
// Example: Creating a multiplication table or 2D array traversal

// ------------------------------------------
// COMMON USE CASES
// ------------------------------------------

// Processing array elements
// Repeating actions a specific number of times
// Searching through collections
// Building tables or grids
// Iterating through object properties
// Filtering or transforming data

// ------------------------------------------
// KEY CONCEPTS
// ------------------------------------------

// Loop Counter: Variable that tracks iterations
// Iteration: One cycle of the loop
// Infinite Loop: Loop that never ends (usually a mistake)
// Loop Condition: Expression that determines when to stop

// ------------------------------------------
// BEST PRACTICES
// ------------------------------------------

// 1. Avoid infinite loops
// 2. Use the right loop type for your task
// 3. Keep loop logic simple and readable
// 4. Use meaningful variable names
// 5. Consider array methods like forEach, map, filter for modern JavaScript

